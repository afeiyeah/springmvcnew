package springmvc.model.bean;

public class City {
	private int cityid;
	private int cityname;
	public int getCityid() {
		return cityid;
	}
	public void setCityid(int cityid) {
		this.cityid = cityid;
	}
	public int getCityname() {
		return cityname;
	}
	public void setCityname(int cityname) {
		this.cityname = cityname;
	}
	
}
